#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*pts;

	pts = s;
	i = 0;
	while (pts[i] != '\0' && i < n)
	{
		pts[i] = 0;
		i++;
	}
}
